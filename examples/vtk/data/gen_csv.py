#!/usr/bin/env python3

# ==============================================================================
# ==============================================================================
# ==============================================================================

import numpy as np
import math

# ==============================================================================
# ==============================================================================
# ==============================================================================

if __name__ == "__main__":

    N = 64
    NX = 401
    NV = 5
    VMAX = 1.0

    x = np.linspace(-VMAX, VMAX, NX)

    for t in range(N):
        with open(f"sines_{t:02d}.csv", "w+") as fp:
            for vx in x:
                lineStr = f"{vx:8.4f}"

                for n in range(NV):
                    v = math.sin((float(vx) / float(VMAX * 2.0) * float(n) + float(t) / float(N)) * (2.0 * np.pi))
                    lineStr += f",{v:16.12f}"

                fp.write(lineStr + "\n")
